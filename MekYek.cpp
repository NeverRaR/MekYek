void OriginalPriority(int color) {//改变搜索方向的顺序
	if (color == 1) {
		Translate[0] = UP_RIGHT; Translate[1] = DOWN; Translate[2] = UP_LEFT; Translate[3] = RIGHT;
		Translate[4] = UP; Translate[5] = DOWN_RIGHT; Translate[6] = LEFT; Translate[7] = DOWN_LEFT;
	}
	else {
		Translate[0] = UP; Translate[1] = DOWN_LEFT; Translate[2] = DOWN_RIGHT; Translate[3] = UP_RIGHT;
		Translate[4] = DOWN; Translate[5] = RIGHT; Translate[6] = UP_LEFT; Translate[7] = LEFT;
	}
}
int ValidMove(int depth, int role) {
	struct Command command;
	int count = 0;
	for (int k = 0; k < 8; k++) {
		const int* delta = DIR[Translate[k]];
		for (int x = 0; x < BOARD_SIZE; x++) {
			for (int y = 0; y < BOARD_SIZE; y++) {
				if (Chessboard[depth][x][y] == Color[role]) {
					int new_x = x + delta[0];
					int new_y = y + delta[1];
					if (ISINBOARD(new_x, new_y) && Chessboard[depth][new_x][new_y] == EMPTY) {
						command.x = x;
						command.y = y;
						command.option = Translate[k];
						PosMove[depth][count++] = command;
					}
				}
			}
		}
	}
	return count;
}
int AlphaBata(int depth, int role, int Alpha, int Beta, int Maxdepth) {
	int Score, i;
	int Location = Key[depth] % HashSize;
	if (!IsFinal) {//如果不处于终局状态，则调用Hash中已经储存的Value
		if (FoundValue[Location].key == Key[depth])
			if (FoundValue[Location].depth >= Maxdepth - depth && depth != 0) {
				return FoundValue[Location].value;
			}
	}
	if (depth == Maxdepth) {
		if (IsFinal) Score = FinalValuate(depth);
		else Score = Valuate(depth);
		FoundValue[Location].key = Key[depth];
		FoundValue[Location].value = Score;
		FoundValue[Location].depth = Maxdepth - depth;
		return Score;
	}
	else {
		if (role == AI) {//AI的回合，选择最大值
			int Tmp;
			Score = MININT;
			int PosNum = ValidMove(depth, role);
			SortCommand(depth, PosNum, role);
			for (i = 0; i < PosNum; ++i) {
				if (Alpha >= Beta) {
					return MAXINT;//被剪去的分支，返回一个不影响上一层决策的值,此时上一层选择最小值
				}
				MyMove(PosMove[depth][i], depth + 1);
				Tmp = AlphaBata(depth + 1, role ^ 1, Alpha, Beta, Maxdepth);
				if (Score < Tmp) {
					Score = Tmp; Alpha = Score;
					Best[depth] = PosMove[0][i];
				}
			}
		}
		else {//对手的回合，选择最小值
			int PosNum = ValidMove(depth, role);
			if (depth < Maxdepth - 1)SortCommand(depth, PosNum, role);
			int Tmp;
			Score = MAXINT;
			for (i = 0; i < PosNum; ++i)
			{
				if (Alpha >= Beta) {
					return MININT;//被剪去的分支，返回一个不影响上一层决策的值，此时上一层选择最大值
				}
				MyMove(PosMove[depth][i], depth + 1);
				Tmp = AlphaBata(depth + 1, role ^ 1, Alpha, Beta, Maxdepth);
				if (Score > Tmp) {
					Score = Tmp; Beta = Score;
				}
			}
		}
	}
	FoundValue[Location].key = Key[depth];
	FoundValue[Location].value = Score;
	FoundValue[Location].depth = Maxdepth - depth;
	return Score;
};
